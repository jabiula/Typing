
public class KeyText {
String upper,lower;
int size;

public KeyText(String upper, String lower,int size) {
	super();
	this.upper = upper;
	this.lower = lower;
	this.size = size;
}

public String getUpper() {
	return upper;
}

public String getLower() {
	return lower;
}
public int getSize()
{
	return size;
}

}
